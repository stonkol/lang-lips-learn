//
package main

import "fmt"

func main()  {
  var bye = "Python"
  fmt.Println(bye)

  var Paul, John int= 1, 2
  fmt.Println(Paul, John)

  var News = true
  fmt.Println(News)

  // ':=' is shorthand for declaring and initializing a variable,
  // e.g. for var f string = "apple" in this case. 
  // This syntax is only available inside functions.
  papaya := "red"
  fmt.Println(papaya)
}
